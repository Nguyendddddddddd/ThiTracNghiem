    public void selectDaTa() {
        Vector vector = null;
        tableModel.setRowCount(0);
        try {
            Connection c = Connection_CSDL.getConnection();
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM `dekiemtra`");
            while (rs.next()) {
                vector = new Vector();
                vector.add(rs.getString(1));
                vector.add(rs.getString(2));
                vector.add(rs.getTime(4));
                vector.add(rs.getInt(5));
                tableModel.addRow(vector);
            }
            jTable_DSDeThi.setModel(tableModel);

        } catch (SQLException ex) {
            Logger.getLogger(FormTaoDe.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
