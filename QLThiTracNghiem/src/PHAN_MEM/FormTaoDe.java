    private void btnSuaDeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaDeActionPerformed
        String maDe = txtMaDe.getText().trim();
        String tenDe = txtTenDe.getText().trim();
        int TGLamBai = (int) jSpinner_TGLamBai.getValue();
        int soCauHoi = (int) jSpinner_SoCauHoi.getValue();
        
        int gio=0;
        int phut=0;
        if (TGLamBai > 59) {
            gio = TGLamBai / 60;
            phut = TGLamBai % 60;
        }
        else
            phut=TGLamBai;

        LocalTime time = LocalTime.of(gio, phut, 0);
        
        Connection_CSDL.UpdateData(tenDe, time, soCauHoi, maDeSua);
        selectDaTa();
        txtMaDe.setText("");
        txtTenDe.setText("");
        jSpinner_SoCauHoi.setValue(1);
        jSpinner_TGLamBai.setValue(1);
        txtMaDe.enable(true);
    }//GEN-LAST:event_btnSuaDeActionPerformed

